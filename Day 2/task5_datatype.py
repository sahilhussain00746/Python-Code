#To print character of a string
print("Hello"[0]) #It will only print the 0th index character

#to print the last character
print("Hello"[0]) #this will print o
print("Hello"[-1]) #this will also print the last character
#like that doing minus, it will print the character from the last
print("Hello"[-2]) #now it will print the character l

#Integer = Whole number
print(325+652) #977

#large number
print(254,478,454) #there python removes the comma
print(458_478_145) #this is a integer and python will remove the underscore'_'

#float = floating point number
print(3.14)

#Boolean
print(True)
print(False)