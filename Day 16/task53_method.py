from turtle import Turtle, Screen
my_screen = Screen()
my_turtle = Turtle()

my_turtle.shape("turtle")
"""This will show you a turtle on the screen"""

my_turtle.color('black', 'red') #Like this you can do color on the turtle

my_turtle.forward(50)

my_screen.exitonclick() 
"""After run this code a screen pop up and it 
run untill you don't click on the screen"""

